def addition(num1 = 9, num2 = 3)
  num1 + num2
end

def subtraction(num1 = 9, num2 = 3)
  num1 - num2
end

def division(num1 = 9, num2 = 3)
  num1 / num2
end

def multiplication(num1 = 9, num2 = 3)
  num1 * num2
end

def modulo(num1 = 9, num2 = 3)
  num1 % num2
end

def square_root(num1 = 16)
  Math.sqrt(num1)
end

def order_of_operation(num1 = 5, num2 = 5, num3 = 5, num4 = 5)
  num1 + (( num2 * num3 ) / num4 )
end
